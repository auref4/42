#include "minitalk.h"

void	ft_lst_clear(t_lst **lst)
{
	t_lst	*tmp;

	while (*lst)
	{
		tmp = (*lst)->next;
		free (*lst);
		*lst = tmp;
	}
	*lst = NULL;
}
